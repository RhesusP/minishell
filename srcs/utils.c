#include "./../includes/minishell.h"

char	*ft_strndup(const char *s1, unsigned int size)
{
	char			*res;
	unsigned int	i;

	res = malloc(sizeof(char) * (size + 1));
	if (!res)
		return (res);
	i = 0;
	while (i < size && s1[i] != '\0')
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' && s1[i] == s2[i])
		i++;
	if (s1[i] == s2[i])
		return (0);
	else
		return (s1[i] - s2[i]);
}

